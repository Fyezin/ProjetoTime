
public class Time {
	private int HR;
	private int Min;
	private int Seg;
	private int HU;
	private int HP;
	private int AM;
	private int PM;
	public int getHR() {
		return HR;
	}
	public void setHR(int hr) {
		HR = hr;
	}
	public int getMin() {
		return Min;
	}
	public void setMin(int min) {
		Min = min;
	}
	public int getSeg() {
		return Seg;
	}
	public void setSeg(int seg) {
		Seg = seg;
	}
	public int getHU() {
		return HU;
	}
	public void setHU(int hu) {
		HU = hu;
	}
	public int getHP() {
		return HP;
	}
	public void setHP(int hp) {
		HP = hp;
	}
	public int getAM() {
		return AM;
	}
	public void setAM(int am) {
		AM = am;
	}
	public int getPM() {
		return PM;
	}
	public void setPM(int pm) {
		PM = pm;
	}
	
	
}
